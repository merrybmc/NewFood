const comment_Input1 = document.querySelector(".comment_Input1");
const comment_List1 = document.querySelector(".comment_List1");
const comment_Add1 = document.querySelector(".comment_Add1");

function add_Comment1() {
  const comment_Text = comment_Input1.value;

  if (comment_Text != "") {
    const comment_item = document.createElement("li");
    comment_item.setAttribute("class", "comments");

    const comment_itemText = document.createElement("span");
    comment_itemText.setAttribute("class", "itemText");
    comment_itemText.innerHTML = "<b>ID</b>&nbsp&nbsp" + comment_Text;

    comment_item.appendChild(comment_itemText);
    comment_List1.appendChild(comment_item);
  } else {
    return alert("댓글을 입력해주세요.");
  }
}

comment_Add1.addEventListener("click", add_Comment1);

